<h3>Formative and Summative Assessments</h3>

<p>
  The baseline grade will come from a student's performances on summative
  assessments (tests, projects, reports, etc.). Formative assessments are
  prerequisites for summative assessments (i.e. a student cannot take a test
  until they are prepared). If I think the summative assessments do not
  accurately reflect a student's knowledge and abilities, I may reference their
  formative assessments to positively adjust their grade.
</p>

<h3>Late Work, Absent Work, and Recovery</h3>

<p>
  I will do my best to make sure every student has the opportunity to learn and
  demonstrate learning. <em
    >All assignments, except tests and any term projects as indicated, can be
    redone to recover points.</em
  > Because things happen sometimes, I will not take off points for late/absent/missing
  work. If students are going on a pre-planned trip, I expect them to reach out in
  advance about what they will be missing. If a student falls significantly behind,
  I am happy to help them formulate a plan of how to help them catch up in a manageable
  way. Death-bed repentance for procrastination is frowned upon. I will be in touch
  with parents as needed.
</p>

